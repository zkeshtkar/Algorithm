def mergeSort(arr):
    if len(arr) > 1:
        mid = len(arr) // 2
        L = arr[:mid]
        R = arr[mid:]
        mergeSort(L)
        mergeSort(R)

        i = j = k = 0
        while i < len(L) and j < len(R):
            if L[i] < R[j]:
                arr[k] = L[i]
                i += 1
            else:
                arr[k] = R[j]
                j += 1
            k += 1
        while i < len(L):
            arr[k] = L[i]
            i += 1
            k += 1

        while j < len(R):
            arr[k] = R[j]
            j += 1
            k += 1
        return arr


def maxMin(k, arr):
    arr = mergeSort(arr)
    res = -1
    for i in range(0, len(arr) - k + 1, 1):
        print(arr[i])
        if res > arr[i + k - 1] - arr[i] or res == -1:
            res = arr[i + k - 1] - arr[i]
    return res


